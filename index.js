const getWhisperResponse = async (mp3Path, message) => {
    const formData = new FormData();

    formData.append('model', 'whisper-1');
    let mp3File = fs.createReadStream(mp3Path);
    formData.append('file', mp3File, 'input.mp3');

    try {
        const { data } = await axiosInstance.post(
            'v1/audio/transcriptions',
            formData,
            { headers: {"Content-Type": "multipart/form-data" }
        })
        return data.text;
        // return data.data[0].url
    } catch (e) {
        console.log(e);
        return `âŒ OpenAI Whisper Response Error`
    }
}

const commands = async (message) => {
    const iaCommands = {
        davinci3: "/bot",
        dalle: "/img",
        whisper: "/whisper"
    }
    let firstWord = message.body.substring(0, message.body.indexOf(" "))
    const isAudio = message.type == 'ptt' && message._data.mimetype === 'audio/ogg; codecs=opus'
    if (isAudio) {
        firstWord = '/whisper'
    }
   /*
    * Faremos uma validaÃ§Ã£o no message.from
    * para caso a gente envie um comando
    * a response nÃ£o seja enviada para
    * nosso prÃ³prio nÃºmero e sim para 
    * a pessoa ou grupo para o qual eu enviei
    */
    const sender = message.from.includes(process.env.PHONE_NUMBER) ? message.to : message.from
    switch (firstWord) {
        case iaCommands.davinci3:
            const question = message.body.substring(message.body.indexOf(" "));
            getDavinciResponse(question).then(async (response) => {
                const contact = await message.getContact()
                client.sendMessage(sender, `${response}\n\n_Generated by @${contact.id.user}_`, { mentions: [contact] })
            })
            break

        case iaCommands.dalle:
            const imgDescription = message.body.substring(message.body.indexOf(" "));
            const contact = await message.getContact();
            getDalleResponse(imgDescription, message).then(async (imgUrl)  => {
                const media = await MessageMedia.fromUrl(imgUrl)
                // Caso queira mandar como Sticker, acrescente em options -> sendMediaAsSticker: true
                const options = {
                    mentions: [contact], 
                    caption: `_Generated by @${contact.id.user}_`, 
                    media: media,
                    sendMediaAsSticker: true,
                }
                await client.sendMessage(sender, media, options)
            })
            break
        case iaCommands.whisper:
            const mp3Path = await downloadMessageMedia(message);

            getWhisperResponse(mp3Path, message).then(async (response) => {
                // const contact = await message.getContact()
                message.reply(`ğŸ¤– ${response}`)
                // client.sendMessage(sender, `ğŸ¤–${response}\n\_Transcrito by @${contact.id.user}_`, { mentions: [contact] })

                getDavinciResponse(`${response}`).then(async (summary) => {
                    // const contact = await message.getContact()
                
                    // message.reply(sender, `${summary}`)
                    message.reply(summary)
                    // client.sendMessage(sender, `${summary}\n_Resumo by @${contact.id.user}_`, { mentions: [contact] })
                })
            })
    }
}
